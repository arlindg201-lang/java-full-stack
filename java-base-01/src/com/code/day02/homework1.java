package com.code.day02;

public class homework1 {
    public static void main(String[] args) {
        double x = 100;
        double y = 20.6;

        double addResult = x + y;
        System.out.println("x,y的和为：" + addResult);

        double subResult = x - y;
        System.out.println("x,y的差为：" + subResult);

        double mulResult = x * y;
        System.out.println("x,y的积为：" + mulResult);

        double divResult = x / y;
        System.out.println("x,y的商为：" + divResult);

    }
}
